package exam03retake01;

public class Contact {

    private String name;
    private String emailAdress;

    public Contact(String name, String emailAdress) {
        this.name = name;
        this.emailAdress = emailAdress;
    }

    public String getName() {
        return name;
    }

    public String getEmailAdress() {
        return emailAdress;
    }
}
